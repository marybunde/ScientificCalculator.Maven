public class Operators {
    public Double Addition(double num1,double num2){
        double solution;
        solution = num1 + num2;
        return solution;}

    public Double Subtraction (double num1,double num2){
        double solution;
        solution = num1 - num2;
        return solution;}

    public Double Multiplication (double num1,double num2){
        double solution;
        solution = num1 * num2;
        return solution;
}
}
